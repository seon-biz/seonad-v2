/**
 * 카테고리별로 묶인 항목 — 렌더링과 Schema 양쪽에서 사용.
 */
export function faqByCategory(): Array<{ meta: FAQCategoryMeta; items: FAQItem[] }> {
  return FAQ_CATEGORIES.map((meta) => ({
    meta,
    items: FAQ_ITEMS.filter((f) => f.category === meta.key),
  }));
}

/**
 * FAQPage Schema 용 텍스트 정규화 — §14-2
 * - 줄바꿈을 공백으로 치환
 * - 연속 공백을 하나로 압축
 */
export function normalizeAnswerForSchema(answer: string): string {
  return answer.replace(/\s+/g, ' ').trim();
}
